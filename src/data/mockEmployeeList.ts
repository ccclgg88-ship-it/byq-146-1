export function hasExpiringContractFn(contracts: EmployeeProfile['contracts']): boolean {
  if (!contracts || contracts.length === 0) return false;
  return contracts.some((c) => {
    const { status } = computeContractStatus(c);
    return status === '即将到期';
  });
}
